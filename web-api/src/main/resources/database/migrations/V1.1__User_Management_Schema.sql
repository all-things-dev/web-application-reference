-- customers

DROP TABLE IF EXISTS customers CASCADE;

CREATE TABLE customers
(
	id      BIGINT  NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

	version INTEGER NOT NULL DEFAULT 0
);

-- user_types

DROP TABLE IF EXISTS user_role_types CASCADE;

CREATE TABLE user_role_types
(
	id                 INTEGER   NOT NULL PRIMARY KEY,

	name               TEXT      NOT NULL,

	creation_timestamp TIMESTAMP NOT NULL DEFAULT CLOCK_TIMESTAMP()
);

INSERT INTO user_role_types (id, name) VALUES (1, 'System'), (2, 'Application');

-- user_types

DROP TABLE IF EXISTS user_roles CASCADE;

CREATE TABLE user_roles
(
	id                 INTEGER   NOT NULL PRIMARY KEY,

	role_type_id       INTEGER   NOT NULL REFERENCES user_role_types (id) ON UPDATE CASCADE ON DELETE CASCADE,

	name               TEXT      NOT NULL,

	creation_timestamp TIMESTAMP NOT NULL DEFAULT CLOCK_TIMESTAMP()
);

INSERT INTO user_roles (id, role_type_id, name) VALUES (1, 1, 'Administrator'), (2, 2, 'Application');

-- users

DROP TABLE IF EXISTS users CASCADE;

CREATE TABLE users
(
	id             BIGINT  NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,

	customer_id    BIGINT  NULL REFERENCES customers (id) ON UPDATE CASCADE ON DELETE CASCADE,
	role_id        INTEGER NOT NULL REFERENCES user_roles (id) ON UPDATE CASCADE ON DELETE CASCADE,

	identity       TEXT    NOT NULL,
	email          TEXT    NOT NULL DEFAULT '',
	contact_number TEXT    NOT NULL DEFAULT '',

	version        INTEGER NOT NULL DEFAULT 0
);

