CREATE TABLE authentication_types
(
	id                 INTEGER   NOT NULL PRIMARY KEY,

	name               TEXT      NOT NULL,

	creation_timestamp TIMESTAMP NOT NULL DEFAULT CLOCK_TIMESTAMP()
);

