-- user_identity_types

DROP TABLE IF EXISTS user_identity_types CASCADE;

CREATE TABLE user_identity_types
(
	id                 INTEGER   NOT NULL PRIMARY KEY,

	name               TEXT      NOT NULL,

	creation_timestamp TIMESTAMP NOT NULL DEFAULT CLOCK_TIMESTAMP()
);

INSERT INTO user_identity_types (id, name)
